// 과제1 정적 재현 스케치 (600x400 기준)
// tip: 색상만 조금 다르면 HSB로 바꿔도 됩니다. 지금은 RGB+알파 사용.

function setup() {
  createCanvas(600, 400);
  noStroke();
  // 바탕(미색)
  background(246, 241, 233); // #F6F1E9 느낌

  // --- 뒤쪽 반투명 레이어들 (먼저 그릴수록 뒤로 감) ---

  // 좌상단 연한 주황 삼각형
  fill(255, 188, 121, 140);
  triangle(40, 55, 260, 60, 120, 180);

  // 좌중단 민트 사각형
  fill(127, 199, 190, 160);
  rect(70, 180, 260, 110, 6);

  // 우상단 보랏빛 사각형
  fill(187, 164, 255, 160);
  rect(420, 40, 110, 170, 6);

  // 우하단 연녹 삼각형(오른쪽 모서리 큰 면)
  fill(173, 209, 140, 170);
  triangle(560, 130, 592, 390, 420, 260);

  // 좌하단 크림 원
  fill(255, 236, 200, 170);
  ellipse(95, 345, 100, 100);

  // 중앙 아래 분홍 원
  fill(255, 120, 160, 190);
  ellipse(300, 340, 70, 70);

  // 우중단 겹치는 초록 원(뒤)
  fill(120, 190, 120, 170);
  ellipse(430, 260, 110, 110);

  // 우중단 겹치는 올리브 원(앞)
  fill(160, 170, 90, 150);
  ellipse(460, 260, 120, 120);

  // 우상단 파란 원(보라 사각형 위)
  fill(130, 170, 255, 190);
  ellipse(520, 120, 110, 110);

  // 좌상단 작은 주황 원
  fill(255, 193, 90, 190);
  ellipse(210, 115, 60, 60);

  // 하단 큰 붉은 다각형(사선 패널 느낌)
  fill(233, 110, 110, 150);
  beginShape();
  vertex(120, 260);
  vertex(550, 310);
  vertex(520, 360);
  vertex(100, 330);
  endShape(CLOSE);

  // 우중단 분홍 패널
  fill(255, 160, 210, 170);
  rect(380, 200, 180, 120, 6);

  // --- 교차하는 대각선 두 개 ---
  strokeWeight(6);
  // 회색 선
  stroke(120, 110, 110, 180);
  line(170, 310, 540, 80);
  // 자주색 선
  stroke(194, 92, 115, 200);
  line(120, 210, 560, 340);
  noStroke();

  // --- 중앙 얼굴(검은 고리 + 노란 얼굴 + 눈 + 점점 미소) ---
  // 검은 고리
  fill(36, 34, 58);            // 진남보(네이비 퍼플)
  ellipse(300, 200, 190, 190); // 바깥 고리
  // 얼굴
  fill(250, 202, 112);         // 노랑피치
  ellipse(300, 200, 150, 150);

  // 눈
  fill(65, 55, 55);
  ellipse(270, 190, 14, 14);
  ellipse(330, 190, 14, 14);

  // 점박이 입 (아치형으로 10개 점)
  const cx = 300, cy = 220, r = 36;
  for (let i = -4; i <= 4; i++) {
    let ang = map(i, -4, 4, -0.9, 0.9);
    ellipse(cx + r * ang, cy + 10 * (1 - abs(ang)), 8, 8);
  }

  // 테두리(아주 얇은 그림자 느낌)
  noFill();
  stroke(120, 110, 100, 100);
  strokeWeight(2);
  rect(2, 2, width-4, height-4, 2);
}
